"""
Post-Award Management — MVP #7
================================
Option tracking, modification routing, CPARS alerts, cure/show cause notices,
and contract closeout management.

Integrates with:
- PolicyService Q-code branches: Q048–Q057 (mods), Q054 (options), Q068–Q077 (post-award), Q108–Q117 (closeout)
- WorkflowGateEngine: Post-Award and Closeout phases
- CompletenessValidator: D129 (Mod Package), D143 (Closeout Checklist), D144 (Final CPARS), D145 (Release of Claims)

Hard Stop Awareness:
- Termination for Default is Tier 3 (FAR 49, FAR 7.503(b)) — AI prohibited
- Ratification is Tier 3 (HCA or designee)
- COR/CO oversight decisions supported but not automated
"""
from __future__ import annotations

from dataclasses import dataclass, field
from datetime import date, datetime, timedelta, timezone
from enum import Enum
from typing import Any
from uuid import uuid4


# ---------------------------------------------------------------------------
# Enums
# ---------------------------------------------------------------------------

class OptionStatus(str, Enum):
    UPCOMING = "upcoming"          # Within alert window
    PENDING_DECISION = "pending"   # CO must decide
    EXERCISED = "exercised"        # Option picked up
    DECLINED = "declined"          # Option not exercised
    EXPIRED = "expired"            # Window passed without action


class ModType(str, Enum):
    BILATERAL = "bilateral"        # FAR 43.103(a) — both parties
    UNILATERAL = "unilateral"      # FAR 43.103(b) — government only
    ADMINISTRATIVE = "administrative"  # FAR 43.101 — admin change
    CHANGE_ORDER = "change_order"  # FAR 43.2 — changes clause
    REA = "rea"                    # Request for Equitable Adjustment


class ModStatus(str, Enum):
    DRAFTED = "drafted"
    UNDER_REVIEW = "under_review"
    APPROVED = "approved"
    EXECUTED = "executed"
    REJECTED = "rejected"


class CPARSStatus(str, Enum):
    NOT_DUE = "not_due"
    DUE_SOON = "due_soon"          # Within 30 days
    OVERDUE = "overdue"            # Past deadline
    DRAFT = "draft"
    SUBMITTED = "submitted"        # CO submitted, awaiting contractor review
    COMPLETE = "complete"          # Finalized


class PerformanceAction(str, Enum):
    NONE = "none"
    VERBAL_WARNING = "verbal_warning"
    WRITTEN_WARNING = "written_warning"
    CURE_NOTICE = "cure_notice"     # FAR 49.402-3(a)
    SHOW_CAUSE = "show_cause"       # FAR 49.402-3(f)
    # TERMINATION_DEFAULT is Tier 3 — never generated by AI


class CloseoutStatus(str, Enum):
    NOT_STARTED = "not_started"
    IN_PROGRESS = "in_progress"
    PENDING_FINAL_PAYMENT = "pending_final_payment"
    PENDING_RELEASE = "pending_release"
    COMPLETE = "complete"


