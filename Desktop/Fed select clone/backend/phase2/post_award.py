    period_type: str             # "interim" or "final"
    period_start: str            # ISO date
    period_end: str              # ISO date
    due_date: str                # ISO date — must be completed by
    status: CPARSStatus = CPARSStatus.NOT_DUE
    quality_rating: str | None = None         # exceptional/very_good/satisfactory/marginal/unsatisfactory
    schedule_rating: str | None = None
    cost_rating: str | None = None
