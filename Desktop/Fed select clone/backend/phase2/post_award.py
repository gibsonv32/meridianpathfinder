@dataclass
class OptionPeriod:
    """Tracks a single option period on a contract."""
    option_id: str
    contract_id: str
    option_number: int           # e.g., 1, 2, 3, 4
    period_start: str            # ISO date
    period_end: str              # ISO date
    decision_deadline: str       # ISO date — CO must decide by this date
    preliminary_notice_date: str  # ISO date — FAR 17.207 preliminary notice
    estimated_value: float = 0.0
    status: OptionStatus = OptionStatus.UPCOMING
    exercise_date: str | None = None
    exercise_rationale: str | None = None
    exercised_by: str | None = None
    decline_rationale: str | None = None
    audit_log: list[dict] = field(default_factory=list)


@dataclass
class Modification:
    """Tracks a contract modification."""
    mod_id: str
    contract_id: str
    mod_number: str              # e.g., "P00001", "A00001"
    mod_type: ModType
    title: str
    description: str
    estimated_value_change: float = 0.0  # positive = increase
    status: ModStatus = ModStatus.DRAFTED
    scope_impact: str = ""       # within/outside scope determination
    authority: str = ""          # FAR citation
    requires_janda: bool = False  # J&A needed for scope change
    created_by: str = ""
    created_at: str = ""
    reviewed_by: str | None = None
    approved_by: str | None = None
    executed_date: str | None = None
    documents: list[str] = field(default_factory=list)  # D-codes required
    audit_log: list[dict] = field(default_factory=list)


